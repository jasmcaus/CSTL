#include <coreten/clock.h>

// Returns the current time (in clock_t)
double now() {
    return clock();
}

// Get duration between `start` and `end` in seconds.
double duration(clock_t start, clock_t end) {
    return (double)(end - start)/CLOCKS_PER_SEC;
}